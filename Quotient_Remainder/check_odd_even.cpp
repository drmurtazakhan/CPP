#include <iostream>
using namespace std;

int main() {

    short remainder;
    
    // Example: Odd number divide by 2, remainder is 1
    remainder = 10 % 2;
    cout << "10 % 2 remainder is " << remainder << endl;

    // Example: Even number divide by 2, remainder is 0
    remainder = 11 % 2;
    cout << "11 % 2 remainder is " << remainder << endl;


    return 0;
}

